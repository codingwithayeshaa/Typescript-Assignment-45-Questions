// Creating a Variable:
let age = 78;

// Creating a program for determining the stages of life by using If-else chain:
if( age < 2){
    console.log("\n You're just a baby.");
}
else if(age >= 2 && age < 4){
    console.log("\n You're a Toddler.");
}
else if(age >= 4 && age < 13){
    console.log("\n You're a Kid.");
}
else if(age >= 13 && age < 20 ){
    console.log("\n You're a Teenager.");
}
else if(age >= 20 && age < 65 ){
    console.log("\n You're an Adult.");
}
else if(age >= 65){
    console.log("\n You're an Elder.");
}



